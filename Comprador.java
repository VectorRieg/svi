package svi;

public class Comprador {
    String nome;
    int idade;
    String cpf;
    String tipoIngresso;
    String filme;

    public Comprador(String nome, int idade, String cpf, String tipoIngresso, String filme) {
        this.nome = nome;
        this.idade = idade;
        this.cpf = cpf;
        this.tipoIngresso = tipoIngresso;
        this.filme = filme;
    }
}